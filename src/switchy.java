public class switchy{
	
	public static void main (String[] args) {
		
		int appreview = 0;
		
		switch (appreview) {
		
		case 1: System.out.println("Terrible!");
		break;
		
		case 2: System.out.println("Not worth the time.");
		break;
		
		case 3: System.out.println("Ok.");
		break;
		
		case 4:System.out.println("Good.");
		break;
		
		case 5:System.out.println("GREAT!");
		break;
		
		default: System.out.println("No rating");
		}
	}
}